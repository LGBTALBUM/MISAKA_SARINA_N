const resolveContentLocale = () => {
  const stored = localStorage.getItem('msarina.locale') || 'system';
  if (stored !== 'system' && CONTENT_I18N_LOCALES.includes(stored)) return stored;
  const browser = navigator.language || 'en';
  if (browser.toLowerCase().startsWith('ja')) return 'ja';
  if (browser.toLowerCase().startsWith('zh')) return 'zh-TW';
  if (browser.toLowerCase().startsWith('ca')) return 'ca';
  return 'en';
};

const walkTextNodes = (root, visitor) => {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent) return NodeFilter.FILTER_REJECT;
      if (parent.closest('script, style, code, pre, textarea, select')) return NodeFilter.FILTER_REJECT;
      if (!node.nodeValue || !node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach(visitor);
};

const normalizeText = (value) => value.replace(/\s+/g, ' ').trim();

const applyContentI18n = () => {
  const locale = resolveContentLocale();
  const dict = contentTranslations[locale] || {};

  walkTextNodes(document.body, (node) => {
    if (!node.parentElement) return;
    if (!node.parentElement.dataset.originalText) {
      node.parentElement.dataset.originalText = normalizeText(node.nodeValue || '');
    }
    const original = node.parentElement.dataset.originalText;
    if (!original) return;
    const translated = locale === 'en' ? original : dict[original];
    if (translated) node.nodeValue = node.nodeValue.replace(/\S[\s\S]*\S|\S/, translated);
  });

  document.querySelectorAll('[data-i18n-count-template]').forEach((node) => {
    if (!node.dataset.originalTemplateText) node.dataset.originalTemplateText = normalizeText(node.textContent || '');
    const count = node.getAttribute('data-count') || '';
    const template = node.dataset.originalTemplateText;
    const translated = locale === 'en' ? template : dict[template] || template;
    node.textContent = translated.replace('{count}', count);
  });
};

document.addEventListener('DOMContentLoaded', () => {
  window.setTimeout(applyContentI18n, 0);
  document.querySelectorAll('[data-locale-select]').forEach((select) => {
    select.addEventListener('change', () => window.setTimeout(applyContentI18n, 0));
  });
});
